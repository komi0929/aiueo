// 文字のバウンディングボックスを計算
// 実際の描画位置 (canvasWidth*0.5, canvasHeight*0.48) に合わせて計算
// textAlign: 'center', textBaseline: 'middle' を前提
export function measureCharBounds(char, font, canvasWidth, canvasHeight) {
  // canvasHeight が未指定の場合は正方形として扱う（debug等の後方互換）
  if (canvasHeight === undefined) canvasHeight = canvasWidth;

  const canvas = document.createElement('canvas');
  canvas.width = canvasWidth;
  canvas.height = canvasHeight;
  const ctx = canvas.getContext('2d');
  ctx.font = font;
  ctx.textBaseline = 'middle';
  ctx.textAlign = 'center';

  const metrics = ctx.measureText(char);
  const charWidth = metrics.width;

  // textBaseline='middle' での ascent/descent
  // middle基準: 文字の上端 = cy - fontBoundingBoxAscent/2 程度
  // 実測ベースで actualBoundingBoxAscent/Descent を使用
  const ascent = metrics.actualBoundingBoxAscent || canvasHeight * 0.35;
  const descent = metrics.actualBoundingBoxDescent || canvasHeight * 0.15;

  // 描画中心座標（guide-renderer と同じ配置）
  const cx = canvasWidth * 0.5;
  const cy = canvasHeight * 0.48;

  return {
    x: cx - charWidth / 2,
    y: cy - ascent,
    w: charWidth,
    h: ascent + descent,
    cx,
    cy
  };
}

// 正規化座標 [0-1] → キャンバスピクセル座標に変換
export function charToCanvas(normX, normY, bounds) {
  return {
    x: bounds.x + normX * bounds.w,
    y: bounds.y + normY * bounds.h
  };
}

