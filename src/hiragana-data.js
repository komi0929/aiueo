// ストローク中間点を線形補間（stroke-engine用）
export function interpolateStroke(stroke, numPoints = 20) {
  if (stroke.length < 2) return stroke;
  const result = [];
  const totalLen = stroke.length - 1;
  for (let i = 0; i < numPoints; i++) {
    const t = i / (numPoints - 1);
    const idx = t * totalLen;
    const lo = Math.floor(idx);
    const hi = Math.min(lo + 1, totalLen);
    const frac = idx - lo;
    result.push([
      stroke[lo][0] + (stroke[hi][0] - stroke[lo][0]) * frac,
      stroke[lo][1] + (stroke[hi][1] - stroke[lo][1]) * frac
    ]);
  }
  return result;
}
